package cl.bice.integracion.movil.model.resp;

/**
 * Clase que representa el estado de la consulta. * 
 *
 */
public class EstadoResp {

    /**
     * Codigo de la respuesta.
     */
    private String codigoEstado;
    
    /**
     * Glosa de la respuesta.
     */
    private String glosaEstado;

	public String getCodigoEstado() {
		return codigoEstado;
	}

	public void setCodigoEstado(String codigoEstado) {
		this.codigoEstado = codigoEstado;
	}

	public String getGlosaEstado() {
		return glosaEstado;
	}

	public void setGlosaEstado(String glosaEstado) {
		this.glosaEstado = glosaEstado;
	}

	
    
    
    
    
}
